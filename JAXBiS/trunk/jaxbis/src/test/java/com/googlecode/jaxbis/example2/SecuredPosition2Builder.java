package com.googlecode.jaxbis.example2;
import com.googlecode.jaxbis.crypt.KeyRing;

import com.googlecode.jaxbis.api.SecuredObjectBuilder;

import com.googlecode.jaxbis.crypt.SecuredMemberBuilder;
import com.googlecode.jaxbis.api.SecurityType;

/*
 * JAXBIS AUTOGENERATED CLASS
 */

public class SecuredPosition2Builder
    implements SecuredObjectBuilder<Position2, SecuredPosition2> {
    private KeyRing keyRing;
 
    public SecuredPosition2Builder(final KeyRing keyRing) {
        this.keyRing = keyRing;
    }
    public SecuredPosition2 createOrNull(
        final Position2 unsecuredObject) {
        if (unsecuredObject != null) {
            return create(unsecuredObject);
        } else {
            return null;
        }
    }
    
    public SecuredPosition2 create(
        final Position2 unsecuredObject) {
   
        final SecuredPosition2 securedObject = new SecuredPosition2();
        final SecuredMemberBuilder secMemBuilder = new SecuredMemberBuilder(
            keyRing);
       
       

       
        securedObject.setSecuredTitle(
            secMemBuilder.createSecuredMemberOrNull(
            unsecuredObject.getTitle(),
            "title",
            SecurityType.SIGNATURE)
         );


        securedObject.setSecuredSalaryRange(
            secMemBuilder.createSecuredMemberOrNull(
            new com.googlecode.jaxbis.example2.SecuredSalaryRange2Builder(
                keyRing).createOrNull(
                    unsecuredObject.getSalaryRange()),
            "salaryRange",
            SecurityType.ENCRYPTION)
                
         );

        
        return securedObject;
    }
}
