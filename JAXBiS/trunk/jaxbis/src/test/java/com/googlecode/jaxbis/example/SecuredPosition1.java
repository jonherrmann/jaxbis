package com.googlecode.jaxbis.example;

import com.googlecode.jaxbis.crypt.KeyRing;

import com.googlecode.jaxbis.api.SecuredMember;
import com.googlecode.jaxbis.api.SecurityType;
import com.googlecode.jaxbis.api.SecuredObject;
import com.googlecode.jaxbis.crypt.PlainInstanceBuilder;

/*
 * JAXBIS AUTOGENERATED CLASS
 */

public class SecuredPosition1 implements SecuredObject<Position1> {
    
    private java.lang.String title;
    
    public void setTitle(
        final java.lang.String title) {
        this.title = title;
    }
    
    public java.lang.String getTitle() {
        return title;
    }
    private SecuredMember securedSalaryRange;
    
    public void setSecuredSalaryRange(
        final SecuredMember securedSalaryRange) {
        this.securedSalaryRange = securedSalaryRange;
    }
    
    public SecuredMember getSecuredSalaryRange() {
        return securedSalaryRange;
    }
    @Override
    public Position1 getPlainInstance(final KeyRing keyRing) {
        
        final Position1 decrpytedObject = new Position1();

        final PlainInstanceBuilder plainInstanceBuilder = 
            new PlainInstanceBuilder(keyRing);

        decrpytedObject.setTitle(getTitle());

    


        decrpytedObject.setSalaryRange(
            plainInstanceBuilder.createPlainInstanceRecursive(
                getSecuredSalaryRange(),
                com.googlecode.jaxbis.example.SecuredSalaryRange1.class,
                SecurityType.ENCRYPTION)
        );                

        
        return decrpytedObject;
    }
}
